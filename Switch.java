package Program;

public class Switch {
    public static void main(String[] args) {
        char ch='a';
        switch(ch){
            case 't': System.out.println("Hi Tarun");
                    break;
            case 's': System.out.println("Hello");
                    break;
            case 'a': System.out.println("Hello Tarun");
                    break;
            default : System.out.println("Bye see you");
        }
    }
    
}
